// Pi-hole v6 REST API: authenticate then PUT/DELETE the DNS host record.
function pihole_dns(string $domain, string $ip, string $action): string {
    // Step 1: authenticate and get session SID + CSRF token
    $authUrl  = PIHOLE_HOST . '/api/auth';
    $authBody = json_encode(['password' => PIHOLE_API_KEY]);
    $authCtx  = stream_context_create(['http' => [
        'method'  => 'POST',
        'header'  => "Content-Type: application/json\r\nContent-Length: " . strlen($authBody) . "\r\n",
        'content' => $authBody,
        'timeout' => 5,
        'ignore_errors' => true,
    ]]);
    $authRes = @file_get_contents($authUrl, false, $authCtx);
    if ($authRes === false) {
        return "WARNING: Could not reach Pi-hole at " . PIHOLE_HOST . ". DNS record not updated.";
    }
    $authJson = json_decode($authRes, true);
    $sid  = $authJson['session']['sid']  ?? null;
    $csrf = $authJson['session']['csrf'] ?? null;
    if (!$sid) {
        return "WARNING: Pi-hole auth failed (check app password). Response: " . htmlspecialchars($authRes);
    }

    // Step 2: PUT or DELETE /api/config/dns/hosts/{ip}%20{domain}
    $entry  = urlencode($ip . ' ' . $domain);
    $dnsUrl = PIHOLE_HOST . '/api/config/dns/hosts/' . $entry;
    $method = ($action === 'add') ? 'PUT' : 'DELETE';
    $dnsCtx = stream_context_create(['http' => [
        'method'  => $method,
        'header'  => "Content-Type: application/json\r\nX-CSRF-Token: {$csrf}\r\nCookie: SID={$sid}\r\n",
        'timeout' => 5,
        'ignore_errors' => true,
    ]]);
    @file_get_contents($dnsUrl, false, $dnsCtx);

    // v6 returns 201 on add, 204 on delete
    $httpCode = 0;
    foreach ($http_response_header ?? [] as $h) {
        if (preg_match('#^HTTP/\S+\s+(\d+)#', $h, $m)) $httpCode = (int)$m[1];
    }
    if (in_array($httpCode, [200, 201, 204])) {
        $verb = ($action === 'add') ? 'added' : 'deleted';
        return "Pi-hole DNS record {$verb}: {$domain} → {$ip}";
    }
    return "Pi-hole DNS warning (HTTP {$httpCode})";
}

// Apache graceful reload — sends USR1 to PID 1 (Apache master process).
// Equivalent to apachectl graceful; no sudo required inside the container.
function apache_graceful(): string {
    $output = shell_exec('kill -USR1 1 2>&1');
    if ($output === null || trim($output) === '') {
        return "Apache reloaded automatically.";
    }
    return "Apache reload warning: " . htmlspecialchars(trim($output));
}

// Recursively delete a directory and all its contents.
function rrmdir(string $path): bool {
    if (!is_dir($path)) return false;
    $items = new RecursiveIteratorIterator(
        new RecursiveDirectoryIterator($path, RecursiveDirectoryIterator::SKIP_DOTS),
        RecursiveIteratorIterator::CHILD_FIRST
    );
    foreach ($items as $item) {
        $item->isDir() ? rmdir($item->getRealPath()) : unlink($item->getRealPath());
    }
    return rmdir($path);
}

// Return sorted list of site folders, excluding hidden/system entries.
function get_sites(): array {
    $sites = [];
    if (!is_dir(WEBSITES_DIR)) return $sites;
    foreach (scandir(WEBSITES_DIR) as $entry) {
        if ($entry === '.' || $entry === '..') continue;
        if (is_dir(WEBSITES_DIR . '/' . $entry)) $sites[] = $entry;
    }
    sort($sites);
    return $sites;
}

