if ($_SERVER['REQUEST_METHOD'] === 'POST' && isset($_POST['action'])) {
    header('Content-Type: application/json');

    $action = trim($_POST['action'] ?? '');
    $site   = trim($_POST['site']   ?? '');

    if (!preg_match('/^[a-zA-Z0-9_\-]+$/', $site) || strpos($site, '000') === 0) {
        echo json_encode(['success' => false, 'message' => 'Invalid site name. Use letters, numbers, hyphens and underscores only. Names cannot start with "000".']);
        exit;
    }
    if ($site === DEFAULT_SITE) {
        echo json_encode(['success' => false, 'message' => 'Cannot modify the default site.']);
        exit;
    }

    $sitePath  = WEBSITES_DIR . '/' . $site;
    $vhostFile = VHOSTS_DIR   . '/' . $site . '.conf';
    $domain    = $site . '.local';
    $messages  = [];

    // ── Create ────────────────────────────────────────────────────────────────
    if ($action === 'create') {
        if (is_dir($sitePath)) {
            echo json_encode(['success' => false, 'message' => "Site '{$site}' already exists."]);
            exit;
        }
        if (!mkdir($sitePath, 0777, true)) {
            echo json_encode(['success' => false, 'message' => "Failed to create site folder at {$sitePath}."]);
            exit;
        }
        chmod($sitePath, 0777);

