    <div style="text-align:center">
        <h1>&#9679; {$domain}</h1>
        <p>Site created. Replace this file with your content.</p>
    </div>
</body>
</html>
HTML;
        file_put_contents($sitePath . '/index.html', $indexHtml);
        $messages[] = "Created site folder and placeholder index.html.";

        $vhostContent = <<<CONF
# Generated by LAMP Site Manager
<VirtualHost *:80>
    ServerName {$domain}
    DocumentRoot /var/www/html/{$site}

    <Directory /var/www/html/{$site}>
        Options Indexes FollowSymLinks
        AllowOverride All
        Require all granted
    </Directory>

    ErrorLog  \${APACHE_LOG_DIR}/{$site}-error.log
    CustomLog \${APACHE_LOG_DIR}/{$site}-access.log combined
</VirtualHost>
CONF;
        if (file_put_contents($vhostFile, $vhostContent) === false) {
            $messages[] = "WARNING: Could not write vhost config. Check permissions on " . VHOSTS_DIR;
        } else {
            $messages[] = "Created vhost config.";
        }

        $messages[] = pihole_dns($domain, SERVER_IP, 'add');
        $messages[] = apache_graceful();

        echo json_encode(['success' => true, 'message' => implode("\n", $messages)]);
        exit;
    }

    // ── Delete ────────────────────────────────────────────────────────────────
    if ($action === 'delete') {
        if (!is_dir($sitePath)) {
            echo json_encode(['success' => false, 'message' => "Site folder '{$site}' not found."]);
            exit;
        }

        $errors = [];

        if (!rrmdir($sitePath)) {
            $errors[] = "Failed to fully remove site folder.";
        } else {
            $messages[] = "Deleted site folder.";
        }

        if (file_exists($vhostFile)) {
            if (!unlink($vhostFile)) {
                $errors[] = "Failed to remove vhost config.";
            } else {
                $messages[] = "Deleted vhost config.";
            }
        } else {
            $messages[] = "No vhost config found (already removed).";
        }

        $messages[] = pihole_dns($domain, SERVER_IP, 'delete');
        $messages[] = apache_graceful();

        if (!empty($errors)) {
            echo json_encode(['success' => false, 'message' => implode("\n", array_merge($messages, $errors))]);
        } else {
            echo json_encode(['success' => true, 'message' => implode("\n", $messages)]);
        }
        exit;
    }

    echo json_encode(['success' => false, 'message' => 'Unknown action.']);
    exit;
}

