<script>
    let pendingDeleteSite = null;

    function createSite() {
        const input    = document.getElementById('new-site-name');
        const siteName = input.value.trim();
        if (!siteName) { showToast('error', 'Missing Input', 'Please enter a site name.'); input.focus(); return; }
        if (!/^[a-zA-Z0-9_\-]+$/.test(siteName)) {
            showToast('error', 'Invalid Name', 'Letters, numbers, hyphens and underscores only.');
            input.focus(); return;
        }
        const btn = document.getElementById('btn-create');
        btn.disabled = true;
        btn.innerHTML = '<span class="spinner"></span>Creating…';
        postAction({ action: 'create', site: siteName }, function(data) {
            btn.disabled = false;
            btn.innerHTML = '+ Create Site';
            if (data.success) {
                showToast('success', 'Site Created', data.message);
                input.value = '';
                addCard(siteName);
            } else {
                showToast('error', 'Failed', data.message);
            }
        });
    }

    function confirmDelete(site) {
        pendingDeleteSite = site;
        document.getElementById('modal-site-name').textContent = site + '.local';
        document.getElementById('modal-overlay').classList.add('open');
    }

    function closeModal() {
        document.getElementById('modal-overlay').classList.remove('open');
        pendingDeleteSite = null;
    }

    function executeDelete() {
        if (!pendingDeleteSite) return;
        const site = pendingDeleteSite;
        closeModal();
        const btn = document.querySelector(`#card-${CSS.escape(site)} .btn-danger`);
        if (btn) { btn.disabled = true; btn.innerHTML = '<span class="spinner" style="border-top-color:var(--red)"></span>'; }
        postAction({ action: 'delete', site: site }, function(data) {
            if (data.success) {
                showToast('success', 'Site Deleted', data.message, true);
                const card = document.getElementById('card-' + site);
                if (card) {
                    card.style.transition = 'opacity 0.3s';
                    card.style.opacity = '0';
                    setTimeout(() => card.remove(), 320);
                }
                updateCount(-1);
            } else {
                showToast('error', 'Failed', data.message);
                if (btn) { btn.disabled = false; btn.innerHTML = 'Delete'; }
            }
        });
    }

    function postAction(data, callback) {
        const body = Object.entries(data)
            .map(([k, v]) => encodeURIComponent(k) + '=' + encodeURIComponent(v))
            .join('&');
        fetch(window.location.href, {
            method: 'POST',
            headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
            body: body
        })
        .then(r => r.json())
        .then(callback)
        .catch(err => showToast('error', 'Network Error', String(err)));
    }

    function addCard(siteName) {
        const grid = document.getElementById('sites-grid');
        const div  = document.createElement('div');
        div.className = 'site-card';
        div.id = 'card-' + siteName;
        div.style.opacity = '0';
        div.style.transition = 'opacity 0.3s';
        div.innerHTML = `
            <div class="site-info">
                <div class="site-icon">🌐</div>
                <div>
                    <a class="site-label site-link" href="http://${esc(siteName)}.local" target="_blank">${esc(siteName)}</a>
                    <div class="site-domain">${esc(siteName)}.local</div>
                </div>
            </div>
            <button class="btn btn-danger" onclick="confirmDelete('${esc(siteName)}')">Delete</button>
        `;
        grid.appendChild(div);
        requestAnimationFrame(() => div.style.opacity = '1');
        updateCount(+1);
    }

    function updateCount(delta) {
        const badge = document.getElementById('site-count-badge');
        if (!badge) return;
        let n = parseInt(badge.textContent) || 0;
        n += delta;
        badge.textContent = n + ' total';
    }

    function showToast(type, title, body, showPihole = false) {
        const toast = document.getElementById('toast');
        document.getElementById('toast-title').textContent = title;
        let html = body
            .replace(/\n/g, '<br>')
            .replace(/&lt;code&gt;(.*?)&lt;\/code&gt;/g, '<code>$1</code>');
        if (showPihole) {
            html += '<br><br><a href="http://192.168.0.6/admin/settings/dnsrecords" target="_blank" '
                  + 'style="display:inline-block;padding:0.3rem 0.75rem;background:rgba(255,165,2,0.12);'
                  + 'color:#ffa502;border:1px solid rgba(255,165,2,0.3);border-radius:6px;'
                  + 'text-decoration:none;font-size:0.78rem;font-weight:600;">'
                  + '&#8599; Remove DNS record in Pi-hole</a>';
        }
        document.getElementById('toast-body').innerHTML = html;
        toast.className = type;
        toast.style.display = 'block';
        clearTimeout(toast._timer);
        toast._timer = setTimeout(hideToast, 9000);
    }

    function hideToast() { document.getElementById('toast').style.display = 'none'; }

    function esc(s) {
        return s.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');
    }

    document.getElementById('modal-overlay').addEventListener('click', e => { if (e.target === e.currentTarget) closeModal(); });
    document.getElementById('new-site-name').addEventListener('keydown', e => { if (e.key === 'Enter') createSite(); });
    document.addEventListener('keydown', e => { if (e.key === 'Escape') closeModal(); });
</script>
